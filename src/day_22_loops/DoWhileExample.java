package day_22_loops;

public class DoWhileExample {
    public static void main(String[] args) {

        int a = 0;

        do {

            System.out.println(a);
            a++;
        } while (a <= 10);


        int a2 = 0;

        do {

            System.out.println(a2);
            a2++;
        } while (a2 == 10);


    }
}
