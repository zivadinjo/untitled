package day_07_unary_operators;

public class Letters {
    public static void main(String[] args) {

        char letter1 = 'F';


        System.out.println("Original "+ letter1);
        System.out.println("Next four: ");
        System.out.println(letter1++);
        System.out.println(letter1++);
        System.out.println(letter1++);
        System.out.println(letter1++);

    }



}
