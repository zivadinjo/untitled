package day_07_unary_operators;

public class IncrementExample2 {
    public static void main(String[] args) {

        int age = 20;
        System.out.println(age);
        System.out.println(age++);
        System.out.println(age);

        System.out.println(age++);
        System.out.println(age);

        System.out.println(++age);


    }
}
