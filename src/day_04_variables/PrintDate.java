package day_04_variables;

public class PrintDate {

    public static void main(String[] args) {

        int Month = 2;
        int Day = 5;
        int Year = 2022;

        System.out.println(Month +"/"+Day+"/"+Year);
        System.out.println(Month +"-"+Day+"-"+Year);
        System.out.println(Month +"<>"+Day+"<>"+Year);
        System.out.println(Month +"*"+Day+"*"+Year);






    }






}
